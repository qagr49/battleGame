package battleEvent;

public class Monster {

	public String name;
	public double monsterHP;
	public double monsterDamage;
	public int monsterGold;
	

	Monster(String n, double mHP, double mDmg, int mGold){
		name = n;
		monsterHP = mHP;
		monsterDamage = mDmg; 
		monsterGold = mGold;
		
	}
}
